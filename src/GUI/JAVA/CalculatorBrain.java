public class CalculatorBrain {

    public float Suma(float num1, float num2){
        return (num1+num2);
    } 
    
    public float Resta(float num1, float num2){
        return (num1-num2);
    }

    public float Multiplicacion(float num1, float num2){
        return (num1*num2);
    }

    public float Division(float num1, float num2){
        return (num1/num2);
    }
    
}
